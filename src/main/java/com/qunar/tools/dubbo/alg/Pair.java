package com.qunar.tools.dubbo.alg;

/**
 * since 2016/12/8.
 */
public class Pair {

    private Integer one;
    private Integer two;


    public Pair(Integer one, Integer two) {
        this.one = one;
        this.two = two;
    }

    public Integer getOne() {
        return one;
    }

    public void setOne(Integer one) {
        this.one = one;
    }

    public Integer getTwo() {
        return two;
    }

    public void setTwo(Integer two) {
        this.two = two;
    }


}
